const sum = (num1: number,num2: number): number => {
    return num1 + num2 ;
};
const sub = (num1: number,num2: number): number => {
    return num1 - num2 ;
};
const multiple = (num1: number,num2: number): number => {
    return num1 * num2 ;
};
const division = (num1: number,num2: number): number => {
    return num1 / num2 ;
};
const remainder = (num1: number,num2: number): number => {
    return num1 % num2 ;
};

export  {sum,sub,multiple,division,remainder};